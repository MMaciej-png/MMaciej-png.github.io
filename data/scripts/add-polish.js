function addPolish(obj) {
  if (obj === null || typeof obj !== "object") return;
  if (Array.isArray(obj)) {
    obj.forEach(addPolish);
    return;
  }
  if (obj.translations && typeof obj.translations.en !== "undefined") {
    const eng = obj.translations.en ?? "";
    const translated = pl(eng) || obj.translations.pl || "";
    obj.translations.pl = translated;
  }
  for (const [k, v] of Object.entries(obj)) {
    if (k === "translations") continue;
    addPolish(v);
  }
}

